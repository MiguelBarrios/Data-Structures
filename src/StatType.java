/**
 * @author Miguel Barrios Davila
 * @version 26 March 2018 Project 2 Enum that states weather the stat
 *          measurement is a max, min, average, or total
 */
public enum StatType
{
    /**
     * If the measurement is the minimum in the set
     */
    MIN,
    /**
     * If the measurement is the maximum in the set
     */
    MAX,
    /**
     * If the measurement is average of the set
     */
    AVG,
    /**
     * If the measurement is the total of the set
     */
    TOT;
}
